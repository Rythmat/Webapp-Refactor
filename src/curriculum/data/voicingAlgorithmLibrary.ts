/** Lookup by composite key: "id|category" */
export function getVoicingAlgorithm(
  id: string,
  category: string,
): VoicingAlgorithmEntry | undefined {
  return VOICING_ALGORITHM_LIBRARY.find(
    (e) => e.id === id && e.category === category,
  );
}

/** Lookup by ID only (returns first match — use when category is known to be unique) */
export function getVoicingAlgorithmById(
  id: string,
): VoicingAlgorithmEntry | undefined {
  return VOICING_ALGORITHM_LIBRARY.find((e) => e.id === id);
}
