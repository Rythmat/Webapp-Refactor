/** Filter by vibe tag */
export function getProgressionsByVibe(vibe: string): ChordProgressionEntry[] {
  const lower = vibe.toLowerCase();
  return CHORD_PROGRESSION_LIBRARY.filter((p) =>
    p.vibes.some((v) => v.toLowerCase() === lower),
  );
}

/** Filter by style */
export function getProgressionsByStyle(style: string): ChordProgressionEntry[] {
  const lower = style.toLowerCase();
  return CHORD_PROGRESSION_LIBRARY.filter((p) =>
    p.styles.some((s) => s === lower),
  );
}

/** Complexity tiers in ascending order */
const COMPLEXITY_ORDER: Record<string, number> = {
  triad: 1,
  '7th': 2,
  extended: 3,
};

/** Filter by maximum complexity level ('triad' | '7th' | 'extended') */
export function getProgressionsByComplexity(
  maxComplexity: string,
): ChordProgressionEntry[] {
  const maxRank = COMPLEXITY_ORDER[maxComplexity] ?? 99;
  return CHORD_PROGRESSION_LIBRARY.filter(
    (p) => (COMPLEXITY_ORDER[p.complexity] ?? 99) <= maxRank,
  );
}
