/** One-bar patterns only */
export const ONE_BAR_COMPING = COMPING_PATTERNS.filter((p) => p.bars === 1);

/** Two-bar patterns only */
export const TWO_BAR_COMPING = COMPING_PATTERNS.filter((p) => p.bars === 2);

/** Lookup by ID */
export function getCompingPattern(id: string): CompingPatternEntry | undefined {
  return COMPING_PATTERNS.find((p) => p.id === id);
}

/** Filter by genre use (case-insensitive substring match) */
export function getCompingPatternsForGenre(
  genre: string,
): CompingPatternEntry[] {
  const lower = genre.toLowerCase();
  return COMPING_PATTERNS.filter((p) =>
    p.genreUse.toLowerCase().includes(lower),
  );
}
