/** Filter by genre, note count, and bars */
export function getPhraseRhythms(
  genre: string,
  noteCount: number,
  bars?: number,
): PhraseRhythmEntry[] {
  return MELODY_PHRASE_RHYTHM_LIBRARY.filter((p) => {
    const genreMatch = p.genre.toLowerCase().includes(genre.toLowerCase());
    const noteMatch = p.noteCount === noteCount;
    const barMatch = bars === undefined || p.bars === bars;
    return genreMatch && noteMatch && barMatch;
  });
}
