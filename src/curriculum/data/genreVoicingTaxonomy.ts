/** Get all voicings for a genre x level */
export function getVoicingsForGenreLevel(
  genre: string,
  level: number,
): VoicingTaxonomyEntry[] {
  return GENRE_VOICING_TAXONOMY.filter(
    (e) => e.genre === genre && e.level === level,
  );
}

/** Get all voicings for a genre x level x quality */
export function getVoicingsForQuality(
  genre: string,
  level: number,
  qualityId: string,
): VoicingTaxonomyEntry[] {
  return GENRE_VOICING_TAXONOMY.filter(
    (e) => e.genre === genre && e.level === level && e.qualityId === qualityId,
  );
}
