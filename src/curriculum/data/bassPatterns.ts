export function getBassContour(id: string): BassContourEntry | undefined {
  return BASS_CONTOUR_PATTERNS.find((p) => p.id === id);
}

export function getBassRhythm(id: string): BassRhythmEntry | undefined {
  return BASS_RHYTHM_PATTERNS.find((p) => p.id === id);
}

export function getBassRhythmsForGenre(genre: string): BassRhythmEntry[] {
  const lower = genre.toLowerCase();
  return BASS_RHYTHM_PATTERNS.filter((p) =>
    p.section.toLowerCase().includes(lower),
  );
}
